export function getBookById(id) {
  return books.find((book) => book.id === Number(id));
}

export function getFeaturedBooks() {
  return books.filter((book) => book.featured);
}

export function getTrendingBooks() {
  return books.filter((book) => book.trending);
}

export function getBooksByTheme(theme) {
  return books.filter((book) => book.theme === theme);
}

export function getReviewsByBookId(bookId) {
  return reviews.filter((r) => r.bookId === Number(bookId));
}

export function getRelatedBooks(bookId) {
  const book = getBookById(bookId);
  if (!book) return [];
  return books.filter((b) => b.id !== book.id && (b.theme === book.theme || b.ageGroup === book.ageGroup)).slice(0, 4);
}
